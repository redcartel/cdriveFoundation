# strings are sequences

message = "I hope everyone is enjoying Python Foundations so far!"

print(len(message))
print(message[4])

# tuples are sequences

coordinates = (1.5, 7.89, 9.7)
print(len(coordinates))
print(coordinates[0])

# ranges are sequences

therange = range(0, 100)
print(len(therange))
print(therange[50])
